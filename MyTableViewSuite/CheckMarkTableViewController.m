#import "CheckMarkTableViewController.h"
#import "MyTableViewSuiteManager.h"

@interface CheckMarkTableViewController ()
@property (strong, nonatomic) NSMutableDictionary *stateDictionary;
@property (strong, nonatomic) NSArray *phrases;
@end

@implementation CheckMarkTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    self.stateDictionary = [[NSMutableDictionary alloc] init];
    self.phrases = [[NSArray alloc] init];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
}

- (void)viewWillAppear:(BOOL)animated {
    [super viewWillAppear:animated];
    
    [[MyTableViewSuiteManager sharedManager] fetchRandomWordData:^(NSArray *phraseData, NSArray *tableData) {
        self.phrases = phraseData;
        [self.tableView reloadData];
    }];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.phrases.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];
    
    // Configure the cell...
    cell.textLabel.text = self.phrases[indexPath.row];
   
    return cell;
}


